    private void lblAgregarTecnicoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblAgregarTecnicoMouseClicked
        // TODO add your handling code here:
        if (jTable1.getSelectedRow()>=0){
            int rowIndex = jTable1.getSelectedRow();
            long cuit = (long) jTable1.getValueAt(rowIndex, 0);
            VistaRegistroTec vistaRegistroTec = new VistaRegistroTec();
            vistaRegistroTec.setVisible(true);
            vistaRegistroTec.setCuitProveedor(cuit);
        }else{
            JOptionPane.showMessageDialog(this,"No ha seleccionado ningún proveedor");
        }
    }//GEN-LAST:event_lblAgregarTecnicoMouseClicked

    private void btnBuscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuscarMouseClicked
        // TODO add your handling code here:
        try {
            long cuit = Long.parseLong(txtBuscar.getText());
            presentador.buscarProveedor(cuit);
        }
        catch(Exception e) {
            this.mostrarMje("Error en el CUIT o no existe el proveedor");
        }
    }//GEN-LAST:event_btnBuscarMouseClicked

    private void btnEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarMouseClicked
        // TODO add your handling code here:
        if(jTable1.getSelectedRow()>=0){
            int rowIndex = jTable1.getSelectedRow();
            long cuit = (long) jTable1.getValueAt(rowIndex, 0);
            //presentador.buscarProveedor(dni);
            int opc = JOptionPane.showConfirmDialog(null, "¿Está seguro de eliminar el proveedor seleccionado?");
            if(opc == 0){
                presentador.eliminarProveedor(cuit);
            }
            
        } else {
            this.mostrarMje("No ha seleccionado ningun técnico");
        }
    }//GEN-LAST:event_btnEliminarMouseClicked

