    public void llenarTablaProveedores(ArrayList<Proveedor> proveedores, ArrayList<Estado> estados, ArrayList<String> direcciones, double[] calificaciones){
        DefaultTableModel model1 = new DefaultTableModel();
        model1.addColumn("DNI");
        model1.addColumn("Apellido");
        model1.addColumn("Nombre");
        model1.addColumn("Direccion");
        model1.addColumn("Telefono");
        model1.addColumn("Estado");
        tablaTecnicos.setModel(model1);
        
        
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("CUIT");
        model.addColumn("Razón Social");
        model.addColumn("Dirección");
        model.addColumn("Telefono");
        model.addColumn("Calificacion");
        model.addColumn("Estado");
        
        for(int i = 0; i < proveedores.size(); i++){
            model.addRow(new Object[]{
                proveedores.get(i).getCuit(), 
                proveedores.get(i).getRazonSocial(), 
                direcciones.get(i), 
                proveedores.get(i).getTelefono(), 
                calificaciones[i]+"", 
                estados.get(i).getEstado()
            });
        }
        
        jTable1.setModel(model);
    }

