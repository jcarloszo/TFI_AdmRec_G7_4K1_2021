    private void btnModificarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnModificarMouseClicked
    
        if (jTable1.getSelectedRow()>=0){
            int rowIndex = jTable1.getSelectedRow();
            VistaModificarPro modificarpro = new VistaModificarPro();
            modificarpro.setVisible(true);
            modificarpro.buscarProveedor((long) jTable1.getValueAt(rowIndex, 0));
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(this,"No ha seleccionado ningún proveedor");
        }
        
        
    }//GEN-LAST:event_btnModificarMouseClicked

