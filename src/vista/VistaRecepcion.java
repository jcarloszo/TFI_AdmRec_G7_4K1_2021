    private void btnBuscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuscarMouseClicked
        // TODO add your handling code here:
        try {
            long cuit = Long.parseLong(txtCuit.getText());
            presentador.buscarProveedor(cuit);
        } catch (Exception e) {
            this.mostrarMje("Error en el CUIT o no existe");
        }
    }//GEN-LAST:event_btnBuscarMouseClicked

    private void btnAgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAgregarMouseClicked
        // TODO add your handling code here:
        
        try {
            String descripcion = txtDescripcion.getText();
            int garantia = Integer.parseInt(txtGarantia.getText());
            String estado = cmbEstado.getSelectedItem().toString();
            String comentarios = txtComentarios.getText();
            
            presentador.agregarEquipo(descripcion, garantia, estado, comentarios);
        } catch (Exception e) {
            this.mostrarMje("Error en alguno de los campos");
        }
    }//GEN-LAST:event_btnAgregarMouseClicked

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if(jTable1.getSelectedRow()>=0){
            int rowIndex = jTable1.getSelectedRow();
            int codigo = (int) jTable1.getValueAt(rowIndex, 0);
            presentador.eliminarEquipo(codigo);
        }
    }//GEN-LAST:event_jTable1MouseClicked

