    void setearTabla(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Código");
        model.addColumn("Descripción");
        model.addColumn("Garantía (Meses)");
        model.addColumn("Estado");
        model.addColumn("Comentarios");
        jTable1.setModel(model);
    }
    
    @Override
    public void mostrarMje(String mje){
        JOptionPane.showMessageDialog(null, mje);
    }
    
    @Override
    public void listarEstadosEquipos(ArrayList<EstadoEquipo> estados){
        cmbEstado.removeAllItems();
        for(EstadoEquipo estado : estados){
            cmbEstado.addItem(estado.getEstadoEquipo());
        }
        cmbEstado.setSelectedIndex(2);
    }
    
    @Override
    public void mostrarDatosProveedor(Proveedor proveedor){
        lblRazonSocial.setText(proveedor.getRazonSocial());
        lblTelefono.setText(proveedor.getTelefono()+"");
    }
    
    @Override 
    public void listarEquipos(ArrayList<Equipo> equipos, ArrayList<EstadoEquipo> estados){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Código");
        model.addColumn("Descripción");
        model.addColumn("Garantía (Meses)");
        model.addColumn("Estado");
        model.addColumn("Comentarios");
        
        for(int i = 0; i < equipos.size(); i++){
            model.addRow(new Object[]{
                equipos.get(i).getId(), 
                equipos.get(i).getDescripcion(), 
                equipos.get(i).getGarantia(), 
                estados.get(i).getEstadoEquipo(), 
                equipos.get(i).getComentarios()
            });
        }
        jTable1.setModel(model);
    }
    
    @Override
    public void limpiar(){
        txtDescripcion.setText("");
        txtGarantia.setText("");
        txtComentarios.setText("");
        cmbEstado.setSelectedItem(2);
    }
    
    @Override
    public void cerrar(){
        VistaMenu menu = new VistaMenu();
        menu.setVisible(true);
        this.dispose();
    }

